public class StringLong {
    String s;
    Long l;

    public StringLong(String s, Long l){
        this.s = s;
        this.l = l;
    }

    public String getS() {
        return s;
    }

    public void setS(String s) {
        this.s = s;
    }

    public Long getL() {
        return l;
    }

    public void setL(Long l) {
        this.l = l;
    }
}
